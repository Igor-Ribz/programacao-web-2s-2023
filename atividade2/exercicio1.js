
const numA = 10;
const numB = 5;


const soma = numA + numB;
console.log(`Resultado da adição entre ${numA} e ${numB} = ${soma}`);


const subtracao = numA - numB;
console.log(`Resultado da subtração entre ${numA} e ${numB} = ${subtracao}`);


const multiplicacao = numA * numB;
console.log(`Resultado da multiplicação entre ${numA} e ${numB} = ${multiplicacao}`);


const divisao = numA / numB;
console.log(`Resultado da divisão entre ${numA} e ${numB} = ${divisao}`);
